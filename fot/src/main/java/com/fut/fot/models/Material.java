package com.fut.fot.models;

public class Material {
	
	private long codigo_material;
	private String desc_material;
	private String url_material;
	
	public long getCodigo_material() {
		return codigo_material;
	}
	public void setCodigo_material(long codigo_material) {
		this.codigo_material = codigo_material;
	}
	public String getDesc_material() {
		return desc_material;
	}
	public void setDesc_material(String desc_material) {
		this.desc_material = desc_material;
	}
	public String getUrl_material() {
		return url_material;
	}
	public void setUrl_material(String url_material) {
		this.url_material = url_material;
	}
	
	

}
